import * as React from "react"

import { cn } from "@/lib/utils"

function Card({
  className,
  size = "default",
  ...props
}: React.ComponentProps<"div"> & { size?: "default" | "sm" }) {
  return (
    <div
      data-slot="card"
      data-size={size}
      className={cn(
        "group/card flex flex-col [gap:var(--card-spacing)] overflow-hidden rounded-xl bg-card [padding-block:var(--card-spacing)] text-sm text-card-foreground ring-1 ring-foreground/10 [--card-spacing:16px] has-[[data-slot=card-footer]]:pb-0 has-[>img:first-child]:pt-0 data-[size=sm]:[--card-spacing:12px] data-[size=sm]:has-[[data-slot=card-footer]]:pb-0 [&_:first-child>img]:rounded-t-xl [&_:last-child>img]:rounded-b-xl",
        className
      )}
      {...props}
    />
  )
}

